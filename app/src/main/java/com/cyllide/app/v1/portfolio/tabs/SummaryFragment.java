    void summaryVolley(){
        String url = getContext().getResources().getString(R.string.dataApiBaseURL)+"stocks/details";
        summaryHeaders.put("ticker", AppConstants.currTicker);
        summaryQueue = Volley.newRequestQueue(getContext());
        StringRequest stringRequest = new StringRequest(Request.Method.GET, url, new Response.Listener<String>() {
            @Override
            public void onResponse(String response) {
                try {
                    JSONObject jsonObject = new JSONObject(response);
                    companyName.setText(jsonObject.getString("Company Name"));
                    companyAddress.setText(jsonObject.getString("Address"));
                    companyIndustry.setText(jsonObject.getString("Industry"));
                    companySector.setText(jsonObject.getString("Sector"));
                } catch (JSONException e) {
                    e.printStackTrace();
                }
            }
        }, new Response.ErrorListener() {
            @Override
            public void onErrorResponse(VolleyError error) {

            }
        }){
          @Override
          public Map<String,String> getHeaders(){return summaryHeaders;}
        };
        summaryQueue.add(stringRequest);
    }

